package java_new_features.java_7_features;
public class StringInSwitchExample1
{
	public static void main(String[] args)
    {
    	String game="Cricket";
    	switch(game)
    	{
    		case "Hockey":
    		{
    			System.out.println("Lets play Hockey");
    		}
    		break;
    		case "Cricket":
    		{
    			System.out.println("Lets play Cricket");
    		}
    		break;
    		case "Football":
    		{
    			System.out.println("Lets play Football");
    		}
    		break;
    	}
    }
}